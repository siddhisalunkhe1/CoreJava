package com.relationalop;

public class Demo
{
   public static void main(String[] args) {
	int s1=20, s2=5;
	System.out.println(s1>s2);
	System.out.println(s1<s2);
	System.out.println(s1>=s2);
	System.out.println(s1<=s2);
	System.out.println(s1==s2);
	System.out.println(s1!=s2);
	System.out.println(!(s1>s2));
}
}
