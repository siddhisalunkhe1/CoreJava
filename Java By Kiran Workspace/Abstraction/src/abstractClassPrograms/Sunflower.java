package abstractClassPrograms;

public class Sunflower extends Flower
{

	@Override
	public void name() {
		System.out.println("Sunflower");
		
	}

	@Override
	public void smell() {
		System.out.println("Smelling good");
		
	}

	@Override
	public void color() {
		System.out.println("Orange");
		
	}

	@Override
	public void isFork() {
		System.out.println("No");
		
	}

}
